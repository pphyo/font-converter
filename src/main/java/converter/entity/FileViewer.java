package converter.entity;

public class FileViewer {

	private String name;
	private String path;
	private String zawOrUni;
	private String fileOrFolder;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getZawOrUni() {
		return zawOrUni;
	}

	public void setZawOrUni(String zawOrUni) {
		this.zawOrUni = zawOrUni;
	}

	public String getFileOrFolder() {
		return fileOrFolder;
	}

	public void setFileOrFolder(String fileOrFolder) {
		this.fileOrFolder = fileOrFolder;
	}

}